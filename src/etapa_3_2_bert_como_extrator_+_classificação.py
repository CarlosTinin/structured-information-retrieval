def plotar_comparacao_modelos(df_comparacao: pd.DataFrame) -> None:
    """
    Plota gráfico de barras comparando modelos.

    Args:
        df_comparacao: DataFrame com métricas
    """
    print("\n📊 Gerando gráfico comparativo...")

    fig, ax = plt.subplots(figsize=(14, 8))

    x = np.arange(len(df_comparacao))
    width = 0.2

    ax.bar(x - 1.5*width, df_comparacao['Accuracy (média)'], width,
           label='Accuracy', alpha=0.8, yerr=df_comparacao['Accuracy (std)'])
    ax.bar(x - 0.5*width, df_comparacao['Precision (média)'], width,
           label='Precision', alpha=0.8, yerr=df_comparacao['Precision (std)'])
    ax.bar(x + 0.5*width, df_comparacao['Recall (média)'], width,
           label='Recall', alpha=0.8, yerr=df_comparacao['Recall (std)'])
    ax.bar(x + 1.5*width, df_comparacao['F1-Score (média)'], width,
           label='F1-Score', alpha=0.8, yerr=df_comparacao['F1-Score (std)'])

    ax.set_xlabel('Modelos', fontweight='bold', fontsize=12)
    ax.set_ylabel('Score', fontweight='bold', fontsize=12)
    ax.set_title('Comparação de Desempenho entre Modelos\n(Média ± Desvio Padrão)',
                 fontweight='bold', fontsize=14)
    ax.set_xticks(x)
    ax.set_xticklabels(df_comparacao['Modelo'], rotation=15, ha='right')
    ax.legend(loc='lower right', fontsize=10)
    ax.set_ylim(0, 1.1)
    ax.grid(axis='y', alpha=0.3)

    plt.tight_layout()
    plt.savefig('comparacao_modelos.png', dpi=150, bbox_inches='tight')
    print("   ✅ Gráfico salvo: comparacao_modelos.png")
    plt.show()

def plotar_matrizes_confusao(
    predicoes_agregadas: Dict[str, Dict[str, List]],
    label_encoder: LabelEncoder,
    df_comparacao: pd.DataFrame
) -> None:
    """
    Plota matrizes de confusão para os modelos.

    Args:
        predicoes_agregadas: Predições agregadas
        label_encoder: Encoder de labels
        df_comparacao: DataFrame com ranking
    """
    print("\n📊 Gerando matrizes de confusão...")

    # Plotar apenas os 4 modelos (ou menos se houver)
    num_modelos = min(4, len(df_comparacao))

    fig, axes = plt.subplots(2, 2, figsize=(16, 14))
    axes = axes.ravel()

    for idx, nome_modelo in enumerate(df_comparacao['Modelo'].head(num_modelos)):
        preds = predicoes_agregadas[nome_modelo]['preds']
        trues = predicoes_agregadas[nome_modelo]['trues']

        cm = confusion_matrix(trues, preds)

        sns.heatmap(
            cm,
            annot=True,
            fmt='d',
            cmap='Blues',
            xticklabels=label_encoder.classes_,
            yticklabels=label_encoder.classes_,
            ax=axes[idx],
            cbar_kws={'label': 'Contagem'}
        )

        f1 = df_comparacao[df_comparacao['Modelo'] == nome_modelo]['F1-Score (média)'].values[0]

        axes[idx].set_title(f'{nome_modelo}\n(F1-Score: {f1:.4f})',
                           fontweight='bold', fontsize=11)
        axes[idx].set_ylabel('Verdadeiro', fontweight='bold')
        axes[idx].set_xlabel('Predito', fontweight='bold')

    # Remover subplots extras se houver
    for idx in range(num_modelos, 4):
        fig.delaxes(axes[idx])

    plt.suptitle('Matrizes de Confusão - Agregadas (todos os folds)',
                 fontsize=16, fontweight='bold', y=0.995)
    plt.tight_layout()
    plt.savefig('matrizes_confusao.png', dpi=150, bbox_inches='tight')
    print("   ✅ Gráfico salvo: matrizes_confusao.png")
    plt.show()

