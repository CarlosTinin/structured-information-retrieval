def main():
    """Pipeline principal de execução."""

    print("\n" + "🚀"*40)
    print("🎯 PIPELINE DE CLASSIFICAÇÃO COM BERT EMBEDDINGS")
    print("🚀"*40 + "\n")

    # 1. Carregar dataset
    csv_path = "./dataset_normalizado.csv"
    df = carregar_dataset(csv_path)

    # 2. Pré-processar
    df, label_encoder = preprocessar_dados(df)

