    df_comparacao, predicoes_agregadas = gerar_relatorio_comparativo(
        resultados, predicoes, label_encoder
    )

    # 6. Visualizações
    plotar_comparacao_modelos(df_comparacao)
    plotar_matrizes_confusao(predicoes_agregadas, label_encoder, df_comparacao)

    print("\n" + "="*80)
    print("✅ PIPELINE CONCLUÍDO COM SUCESSO!")
    print("="*80)
    print("\n📁 Arquivos gerados:")
    print("   • comparacao_modelos.png")
    print("   • matrizes_confusao.png")
    print()

    return df_comparacao, predicoes_agregadas, label_encoder

